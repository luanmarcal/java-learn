// Exercício 22
// Elaborar um programa que escreva em tela os números de 1 a 20, utilizando o comando do-
// while( ).


public class Exercise22 {
    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println(i);
            i++;
        } while(i <= 20);
    }
}
