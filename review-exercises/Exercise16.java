// Exercício 16
// Elaborar um programa que escreva em tela os números de 1 a 20, utilizando o comando
// while( ).


public class Exercise16 {
    public static void main(String[] args) {

        int num = 1;

        while (num <= 20) {
            System.out.println(num);
            num++;
        }
    }
}
